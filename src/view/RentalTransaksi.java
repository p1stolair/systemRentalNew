    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            pj = new Rental();
            pj.setSewa(Integer.parseInt(txtSewa.getText()) * Integer.parseInt(txtJmlPin.getText()));
            pj.setNotrans(txtNotrans.getText());
            pj.setStok(Integer.parseInt(txtStok.getText()));
            pj.setJmldvd(Integer.parseInt(txtJmlPin.getText()));
            pj.setKodedvd(txtKodedvd.getText());

            int noBaris = tabelTrans.getSelectedRow();
            //jika baris tidak di klik
            if (noBaris < 0) {
                return;
            }

            if (JOptionPane.showConfirmDialog(this, "Yakin menghapus data", "Peringatan", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.CANCEL_OPTION) {
                return;
            }
            //mendapatkan nilai no dari tabel yang di klik
            Object index = tabelTrans.getValueAt(noBaris, 0);
            dao.connect();
            dao.deletedetail(String.valueOf(index), pj);
            List<Rental> list = new ArrayList<Rental>();
            list = dao.listPinjam(txtMember.getText());
            //menampilkan data ke tabel
            TableModel tabelModel = new TabelTransaksi(list);
            tabelTrans.setModel(tabelModel);

            //menampilkan total bayar
            total = total - pj.getSewa();
            txtTotal.setText(String.valueOf(total));
            Nodetail();

            txtKodedvd.setText("");
            txtJudul.setText("");
            txtStok.setText("");
            txtSewa.setText("");
            txtJmlPin.setText("");
            txtKodedvd.setEditable(true);
            txtJmlPin.setEditable(true);
            btnSearch2.setEnabled(true);
            txtTglHarus.setEnabled(true);
            btnGratis.setEnabled(true);
            txtTglHarus.setDate(null);
            txtKodedvd.requestFocus();

            dao.disconnect();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Kesalahan : " + ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnGratisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGratisActionPerformed
        if (txtMember.getText().equals("") && txtKodedvd.getText().equals("") && txtJmlPin.getText().equals("") && tglHarus.equals(null)) {
            JOptionPane.showMessageDialog(null, "Text Field harus diisi semua !!", "Warning", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                if (Integer.parseInt(txtStok.getText()) != 0) {
                    if (Integer.parseInt(txtJmlPin.getText()) <= Integer.parseInt(txtStok.getText())) {
                        pj = new Rental();
                        //generate kode index
                        int jum = String.valueOf(txtDetail.getText()).length();
                        int jumlah = 4 - jum;
                        String nol = "";
                        for (int i = 1; i < jumlah; i++) {
                            nol = nol + "0";
                        }
                        pj.setIndex(txtNotrans.getText() + nol + txtDetail.getText());

                        //mendapatkan inputan di textfield
                        pj.setKodedvd(txtKodedvd.getText().toUpperCase());
                        pj.setNotrans(txtNotrans.getText());
                        pj.setJmldvd(Integer.parseInt(txtJmlPin.getText()));
                        pj.setNodetail(Integer.parseInt(txtDetail.getText()));
                        pj.setSewa(Integer.parseInt(txtSewa.getText()) * Integer.parseInt(txtJmlPin.getText()));
                        pj.setStok(Integer.parseInt(txtStok.getText()));
                        pj.setTglharus(tglHarus);

                        dao.connect();
                        dao.insertgratis(pj);
                        List<Rental> list = new ArrayList<Rental>();
                        //membaca datanya ke list
                        list = dao.listPinjam(txtMember.getText());
                        //menampilkan data ke tabel
                        TableModel tabelModel = new TabelTransaksi(list);
                        tabelTrans.setModel(tabelModel);

                        Nodetail();

                        txtKodedvd.setText("");
                        txtJudul.setText("");
                        txtStok.setText("");
                        txtSewa.setText("");
                        txtJmlPin.setText("");
                        btnDelete.setEnabled(true);
                        txtTglHarus.setDate(null);
                        txtKodedvd.requestFocus();

                        dao.disconnect();
                    } else {
                        JOptionPane.showMessageDialog(null, "Jumlah yang dipinjam melebihi stok", "Warning", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Stok DVD kosong", "Warning", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "no tidak boleh sama ");
            }
        }
    }//GEN-LAST:event_btnGratisActionPerformed

