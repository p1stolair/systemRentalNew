    private void tabelTransMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelTransMouseClicked
        int selected = tabelTrans.getSelectedRow();
        if (selected < 0) {
            JOptionPane.showMessageDialog(this, "Klik row tabel");
        } else {
            //mendapatkan value nomor transaksi di tabel detail trans dan memasukkannya ke text field
            String index = tabelTrans.getValueAt(selected, 0).toString();
            String trans = index.substring(0, 10);
            String detail = index.substring(10);
            int det = Integer.parseInt(detail);
            txtNotrans.setText(trans);
            txtDetail.setText(String.valueOf(det));

            txtKodedvd.setText((String) tabelTrans.getValueAt(selected, 1));
            txtJmlPin.setText((String) tabelTrans.getValueAt(selected, 2).toString());

            //set tanggal harus kembali di text field
            String dateValue = String.valueOf(tabelTrans.getValueAt(selected, 4));
            Date date = new Date();
            try {
                date = new SimpleDateFormat("yyyy-MM-dd").parse(dateValue);
            } catch (ParseException ex) {
                Logger.getLogger(RentalTransaksi.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtTglHarus.setDate(date);

            //mendapatkan value untuk kode dvd
            try {
                dao.connect();
                pj = new Rental();
                dao.getValueDVD(txtKodedvd.getText(), pj);
                if (pj.getRow() == 1) {
                    txtJudul.setText(pj.getJudul());
                    txtStok.setText(String.valueOf(pj.getStok()));
                    txtSewa.setText(String.valueOf(pj.getSewa()));
                    txtJudul.setEditable(false);
                    txtStok.setEditable(false);
                    txtSewa.setEditable(false);
                    txtJmlKbl.requestFocus();
                }
            } catch (SQLException ex) {
                Logger.getLogger(RentalTransaksi.class.getName()).log(Level.SEVERE, null, ex);
            }

            txtTglKbl.setEnabled(true);
            txtJmlKbl.setEditable(true);
            txtKbl.setEditable(true);
            txtBayar.setEditable(true);
            btnProses.setEnabled(true);
            btnCreate.setEnabled(false);

        }
    }//GEN-LAST:event_tabelTransMouseClicked

    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        try {
            pj = new Rental();
            dao.connect();

            //validasi jumlah dvd yang dikembalikan
            if (Integer.parseInt(txtJmlKbl.getText()) <= Integer.parseInt(txtJmlPin.getText())) {
                //memasukkan tanggal kembali ke database
                pj.setKodepeg(txtUserlog.getText());
                pj.setKodedvd(txtKodedvd.getText());
                pj.setJmlkbl(Integer.parseInt(txtJmlKbl.getText()));
                pj.setJmldvd(Integer.parseInt(txtJmlPin.getText()));
                pj.setTglkbl(tglKbl);
                pj.setNodetail(Integer.parseInt(txtDetail.getText()));
                pj.setNotrans(txtNotrans.getText());

                //menyimpan variabel denda
                pj.setDenda(Integer.parseInt(txtDenda.getText()));

                //memasukkan data ke tabel detail transaksi
                dao.transKembali(pj);
                total = total + Integer.parseInt(txtDenda.getText());
                txtTotal.setText(String.valueOf(total));

                //set status untuk peminjaman DVD (Borrowed atau Returned)
                dao.cekJml(pj);
                if (pj.getJmlkbl() == 0) {
                    dao.rubahStat(pj);
                }

                List<Rental> list = new ArrayList<Rental>();
                //membaca datanya ke list
                list = dao.listPinjam(txtMember.getText());
                //menampilkan data ke tabel
                TableModel tabelModel = new TabelTransaksi(list);
                tabelTrans.setModel(tabelModel);

                txtKodedvd.setText("");
                txtNotrans.setText("");
                txtDetail.setText("");
                txtDenda.setText("");
                txtTglHarus.setDate(null);
                txtTglKbl.setDate(null);
                txtJudul.setText("");
                txtStok.setText("");
                txtJmlPin.setText("");
                txtJmlKbl.setText("");
                txtSewa.setText("");

            } else {
                JOptionPane.showMessageDialog(null, "Jumlah DVD yang dikembalikan tidak cocok", "Warning", JOptionPane.ERROR_MESSAGE);
            }
            dao.disconnect();
        } catch (SQLException ex) {
            Logger.getLogger(RentalTransaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnProsesActionPerformed

    private void txtTglKblPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_txtTglKblPropertyChange
        if (txtTglKbl.getDate() != null) {
            SimpleDateFormat Format = new SimpleDateFormat("yyyy-MM-dd");
            tglKbl = Format.format(txtTglKbl.getDate());

            try {
                dao.connect();
                pj = new Rental();
                int tot = 0;

                pj.setTglkbl(tglKbl);
                pj.setTglharus(tglHarus);

                //menghitung denda
                dao.cekDenda(Integer.parseInt(txtDetail.getText()), pj);
                dao.getValueDVD(txtKodedvd.getText(), pj);
                dao.valueGeneral(pj.getStatus(), pj);

                tot = tot + (pj.getLama() / pj.getLamapin() * pj.getDendapin() * Integer.parseInt(txtJmlKbl.getText()));
                tot = tot + (pj.getLama() % pj.getLamapin() * pj.getDendapin() * Integer.parseInt(txtJmlKbl.getText()));
                txtDenda.setText(String.valueOf(tot));

                dao.disconnect();
            } catch (SQLException ex) {
                Logger.getLogger(RentalTransaksi.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_txtTglKblPropertyChange

