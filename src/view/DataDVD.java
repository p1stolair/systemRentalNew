    public DataDVD() {
        initComponents();
        setTitle("HISTORY PEMINJAMAN DVD");
        setLocation(400, 70);
        txtMember.requestFocus();
        pj = new Rental();
        try {
            dao.connect();
            List<Rental> list = new ArrayList<Rental>();
            list = dao.listHistoryDVD(RentalPencarian.getKodeDVD());
            TableModel tableModel = new TabelPencarian(list);
            tabelDVD.setModel(tableModel);
            
            txtKode.setText(RentalPencarian.getKodeDVD());
            dao.getValueDVD(txtKode.getText(), pj);
            
            //menampilkan value DVD
            txtJudul.setText(pj.getJudul());
            txtSewa.setText(String.valueOf(pj.getSewa()));
            txtStok.setText(String.valueOf(pj.getStok()));
            txtKode.setEditable(false);
            txtJudul.setEditable(false);
            txtSewa.setEditable(false);
            txtStok.setEditable(false);
            
            dao.disconnect();
        } catch (SQLException ex) {
            Logger.getLogger(DataDVD.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

