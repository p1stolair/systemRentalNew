    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            //mendapatkan nilai dari yang dicari
            pj=new Rental();
            pj.setKodedvd(txtKode.getText());
            pj.setKodemem(txtMember.getText());
            pj.setTglharus(tglPin1);
            pj.setTglharus2(tglPin2);
            pj.setTglkbl(tglKem1);
            pj.setTglkbl2(tglKem2);
            
            List<Rental> list = new ArrayList<Rental>();
            list = new ArrayList<Rental>();
            dao.connect();
            list = dao.readHistoryDVD(pj);
            //menampilkan yang dicari
            TableModel tableModel = new TabelPencarian(list);
            tabelDVD.setModel(tableModel);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Kesalahan : " + ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

